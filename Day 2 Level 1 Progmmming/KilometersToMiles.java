// Convert kilometers distance into miles  

public class KilometersToMiles{
	public static void main(String []args){
	// define kilometers
	double kilometer=10.8;
	
	// define miles in one kilometer
	double miles=0.6;
	
	// convert distance in miles 
	double distanceInMiles=miles*kilometer;
	// display distance into kilometers
	System.out.println("The distance "+kilometer+"km in miles is "+distanceInMiles);
	
	
	}
}